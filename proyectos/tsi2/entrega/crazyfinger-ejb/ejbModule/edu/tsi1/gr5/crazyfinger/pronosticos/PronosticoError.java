package edu.tsi1.gr5.crazyfinger.pronosticos;

public class PronosticoError extends Exception {

	public PronosticoError() {
		super();
	}

	public PronosticoError(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PronosticoError(String arg0) {
		super(arg0);
	}

	public PronosticoError(Throwable arg0) {
		super(arg0);
	}

}
